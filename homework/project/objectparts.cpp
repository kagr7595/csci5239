QVector3D Fprojopengl::normals(float x0, float y0, float z0,
                               float x1, float y1, float z1,
                               float x2, float y2, float z2){
    float vx0 = x1-x0;
    float vy0 = y1-y0;
    float vz0 = z1-z0;
    float vx1 = x2-x0;
    float vy1 = y2-y0;
    float vz1 = z2-z0;
    float Nx = (vy0*vz1)-(vz0*vy1);
    float Ny = (vz0*vx1)-(vx0*vz1);
    float Nz = (vx0*vy1)-(vy0*vx1);
    QVector3D vec = QVector3D(Nx, Ny, Nz);
    vec.normalize();
    //qInfo() << vec;
    return vec;
}
